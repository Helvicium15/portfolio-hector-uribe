/* ============================================================
   COUNTER ANIMATION (hero meta stats)
   ============================================================ */
function animateCounter(el) {
  const target = parseInt(el.dataset.count, 10);
  const duration = 1400;
  const startTime = performance.now();

  function tick(now) {
    const elapsed = now - startTime;
    const progress = Math.min(elapsed / duration, 1);
    // easeOutExpo
    const eased = progress === 1 ? 1 : 1 - Math.pow(2, -10 * progress);
    el.textContent = Math.floor(eased * target);
    if (progress < 1) requestAnimationFrame(tick);
    else el.textContent = target;
  }
  requestAnimationFrame(tick);
}

/* ============================================================
   HERO KEYBOARD KEYS — hover press effect
   ============================================================ */
document.querySelectorAll('.key-wrap').forEach(key => {
  key.addEventListener('mouseenter', () => {
    key.classList.remove('hovering');
    // Force reflow so animation restarts every hover
    void key.offsetWidth;
    key.classList.add('hovering');
  });
  key.addEventListener('animationend', (e) => {
    if (e.animationName === 'keyPressHover') {
      key.classList.remove('hovering');
    }
  });
});

/* ============================================================
   HERO KEYBOARD KEYS — random press-in animation
   ============================================================ */
(function () {
  const keys = Array.from(document.querySelectorAll('.key-wrap'));
  if (!keys.length) return;

  // Shuffle indices for a random fire order
  const order = keys.map((_, i) => i);
  for (let i = order.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [order[i], order[j]] = [order[j], order[i]];
  }

  // Base delay before first key, interval between each
  const BASE   = 300;   // ms before first key
  const STEP   = 140;   // ms between consecutive presses

  order.forEach((keyIndex, fireIndex) => {
    setTimeout(() => {
      keys[keyIndex].classList.add('pressed');
    }, BASE + fireIndex * STEP);
  });
})();

/* ============================================================
   INTERSECTION OBSERVER — reveal & counters
   ============================================================ */
const revealObserver = new IntersectionObserver(entries => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      entry.target.classList.add('visible');
      revealObserver.unobserve(entry.target);
    }
  });
}, { threshold: 0.12, rootMargin: '0px 0px -50px 0px' });

document.querySelectorAll('.reveal').forEach(el => revealObserver.observe(el));

// Hero meta counters — trigger once hero-meta becomes visible
const heroMeta = document.querySelector('.hero-meta');
if (heroMeta) {
  const counterObserver = new IntersectionObserver(entries => {
    if (entries[0].isIntersecting) {
      document.querySelectorAll('.meta-num').forEach(animateCounter);
      counterObserver.disconnect();
    }
  }, { threshold: 0.5 });
  counterObserver.observe(heroMeta);
}

/* ============================================================
   SKILLS CAROUSEL
   ============================================================ */
const carousel = document.getElementById('carousel');
const prevBtn  = document.getElementById('prevBtn');
const nextBtn  = document.getElementById('nextBtn');
const dotsEl   = document.getElementById('dots');

if (carousel && prevBtn && nextBtn && dotsEl) {
  const cards = carousel.querySelectorAll('.skill-card');
  const dots  = dotsEl.querySelectorAll('.dot');
  let current = 0;

  function goTo(index) {
    cards[current].classList.remove('active');
    dots[current].classList.remove('active');
    current = (index + cards.length) % cards.length;
    cards[current].classList.add('active');
    dots[current].classList.add('active');
  }

  prevBtn.addEventListener('click', () => goTo(current - 1));
  nextBtn.addEventListener('click', () => goTo(current + 1));
  dots.forEach(dot => dot.addEventListener('click', () => goTo(parseInt(dot.dataset.i))));

  // Auto-advance every 5 seconds
  let autoPlay = setInterval(() => goTo(current + 1), 5000);
  carousel.addEventListener('mouseenter', () => clearInterval(autoPlay));
  carousel.addEventListener('mouseleave', () => {
    autoPlay = setInterval(() => goTo(current + 1), 5000);
  });
}

