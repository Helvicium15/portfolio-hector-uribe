let currentLang = 'de';

function applyTranslation(lang) {
  currentLang = lang;
  const t = translations[lang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key] !== undefined) el.textContent = t[key];
  });
  const btn = document.getElementById('langToggle');
  if (btn) btn.textContent = lang === 'de' ? 'EN' : 'DE';
  document.documentElement.lang = lang;
}

/* ============================================================
   CUSTOM CURSOR
   ============================================================ */
const cursorEl  = document.getElementById('cursor');
const cursorDot = document.getElementById('cursorDot');
let mouseX = 0, mouseY = 0;
let curX   = 0, curY   = 0;

if (cursorEl && cursorDot) {
  document.addEventListener('mousemove', e => {
    mouseX = e.clientX;
    mouseY = e.clientY;
    cursorDot.style.transform = `translate(${mouseX - 2.5}px, ${mouseY - 2.5}px)`;
  });

  (function animateCursor() {
    curX += (mouseX - curX) * 0.11;
    curY += (mouseY - curY) * 0.11;
    cursorEl.style.transform = `translate(${curX - 18}px, ${curY - 18}px)`;
    requestAnimationFrame(animateCursor);
  })();

  document.querySelectorAll('a, button, .project-card, .skill-card, .contact-card, .tool').forEach(el => {
    el.addEventListener('mouseenter', () => cursorEl.classList.add('expand'));
    el.addEventListener('mouseleave', () => cursorEl.classList.remove('expand'));
  });
}

