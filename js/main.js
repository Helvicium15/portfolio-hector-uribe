/* ============================================================
   ACTIVE NAV LINK ON SCROLL
   ============================================================ */
const sections = document.querySelectorAll('section[id]');
const navLinks  = document.querySelectorAll('.nav-link');

const sectionObserver = new IntersectionObserver(entries => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      const id = entry.target.id;
      navLinks.forEach(link => {
        link.classList.toggle('active', link.getAttribute('href') === `#${id}`);
      });
    }
  });
}, { rootMargin: '-20% 0px -60% 0px', threshold: 0 });

sections.forEach(s => sectionObserver.observe(s));

/* ============================================================
   SMOOTH ANCHOR SCROLL (respects CSS scroll-behavior)
   ============================================================ */
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
  anchor.addEventListener('click', e => {
    const target = document.querySelector(anchor.getAttribute('href'));
    if (target) {
      e.preventDefault();
      target.scrollIntoView({ behavior: 'smooth', block: 'start' });
    }
  });
});

/* ============================================================
   GALLERY SLIDER + FILTER
   ============================================================ */
const galleryGrid    = document.getElementById('galleryGrid');
const galleryPrev    = document.getElementById('galleryPrev');
const galleryNext    = document.getElementById('galleryNext');
const galleryCountEl = document.getElementById('galleryCount');
const gfBtns         = document.querySelectorAll('.gf-btn');
const CARD_W         = 280 + 20; // card width + gap (1.25rem ≈ 20px)

function updateGalleryNav() {
  if (!galleryGrid || !galleryPrev || !galleryNext) return;
  const atStart = galleryGrid.scrollLeft <= 2;
  const atEnd   = galleryGrid.scrollLeft + galleryGrid.clientWidth >= galleryGrid.scrollWidth - 4;
  galleryPrev.disabled = atStart;
  galleryNext.disabled = atEnd;
}

function updateGalleryCount() {
  if (!galleryCountEl || !galleryGrid) return;
  const visible = galleryGrid.querySelectorAll('.gallery-item:not(.hidden)').length;
  galleryCountEl.textContent = visible;
}

if (galleryGrid) {
  galleryGrid.addEventListener('scroll', updateGalleryNav, { passive: true });

  galleryNext && galleryNext.addEventListener('click', () => {
    galleryGrid.scrollLeft += CARD_W;
    setTimeout(updateGalleryNav, 520);
  });

  galleryPrev && galleryPrev.addEventListener('click', () => {
    galleryGrid.scrollLeft -= CARD_W;
    setTimeout(updateGalleryNav, 520);
  });

  gfBtns.forEach(btn => {
    btn.addEventListener('click', () => {
      const filter = btn.dataset.filter;
      gfBtns.forEach(b => b.classList.remove('active'));
      btn.classList.add('active');

      galleryGrid.querySelectorAll('.gallery-item').forEach(item => {
        const match = filter === 'all' || item.dataset.cat === filter;
        item.classList.toggle('hidden', !match);
      });

      galleryGrid.scrollLeft = 0;
      updateGalleryCount();
      setTimeout(updateGalleryNav, 50);
    });
  });

  // Init state
  updateGalleryCount();
  setTimeout(updateGalleryNav, 100);
}

